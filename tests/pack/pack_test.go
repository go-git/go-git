func (p *PackHandlerSuite[T]) TestFindHash() {
	handler := p.newPackHandler()
	defer handler.Close()

	tests := []struct {
		name    string
		offset  uint64
		want    plumbing.ObjectID
		wantErr string
	}{
		{
			name:    "invalid offset - not found",
			offset:  999999,
			wantErr: "not found",
		},
		{
			name:    "zero offset",
			offset:  0,
			wantErr: "not found",
		},
		{
			name:   "valid commit hash",
			offset: 12,
			want:   plumbing.NewHash("e8d3ffab552895c19b9fcf7aa264d277cde33881"),
		},
		{
			name:   "valid delta commit hash (1 level)",
			offset: 186,
			want:   plumbing.NewHash("6ecf0ef2c2dffb796033e5a02219af86ec6584e5"),
		},
		{
			name:   "valid blob hash",
			offset: 1524,
			want:   plumbing.NewHash("32858aad3c383ed1ff0a0f9bdf231d54a00c9e88"),
		},
		{
			name:   "valid tree hash",
			offset: 84479,
			want:   plumbing.NewHash("5a877e6a906a2743ad6e45d99c1793642aaf8eda"),
		},
		{
			name:   "valid delta tree hash (1 level)",
			offset: 84375,
			want:   plumbing.NewHash("a8d315b2b1c615d43042c3a62402b8a54288cf5c"),
		},
		{
			name:   "valid delta tree hash (3 levels)",
			offset: 84760,
			want:   plumbing.NewHash("aa9b383c260e1d05fbbf6b30a02914555e20c725"),
		},
	}

	for _, tc := range tests {
		p.T().Run(tc.name, func(t *testing.T) {
			hash, err := handler.FindHash(T(tc.offset))
			if tc.wantErr != "" {
				assert.ErrorContains(t, err, tc.wantErr)
				assert.Equal(t, plumbing.ZeroHash, hash)
			} else {
				assert.NoError(t, err)
				assert.Equal(t, tc.want, hash)
			}
		})
	}
}

